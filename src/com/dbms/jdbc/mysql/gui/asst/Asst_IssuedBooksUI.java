    private void checkOverduedBooks(){
        try{
            ArrayList<IssuedBooks> list = issuedDAO.getAllIssuedBooks();
            issuedDAO.overduedBooks();
            int count = 0;
            for(int i = 0;i<list.size();i++){
                if(issuedDAO.checkIfOverdued(list.get(i).getUserID(), list.get(i).getBookID())){
                    count++;
                }
            }
            if(count != 0){
                
                String str = String.format("%s",(count > 1)? "books are" : "book is");
                JOptionPane.showMessageDialog(Asst_IssuedBooksUI.this,
                        "As of this date, " + count + " " + str + " overdued",
                        "Books Overdued",JOptionPane.INFORMATION_MESSAGE);
            }
        
        }
        catch(Exception exc){
            JOptionPane.showMessageDialog(Asst_IssuedBooksUI.this,"Check Failed: "+exc.getMessage(),"Check Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    
