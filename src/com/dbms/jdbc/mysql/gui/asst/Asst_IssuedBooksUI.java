    private void jbtn_RenewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_RenewActionPerformed
        int row = jtbl_IssuedBooks.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(Asst_IssuedBooksUI.this,"Select a row","Renew Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            String bookID = jtbl_IssuedBooks.getValueAt(row,0).toString();
            String bookTitle = jtbl_IssuedBooks.getValueAt(row,1).toString();
            String userID = jtbl_IssuedBooks.getValueAt(row,2).toString();
            Date dateBrwd = (Date)jtbl_IssuedBooks.getValueAt(row,3);
            Date datedue = (Date)jtbl_IssuedBooks.getValueAt(row,4);
            
            IssuedBooks issuedBook = new IssuedBooks(bookID,bookTitle,userID,dateBrwd,datedue);
            Asst_IssuedBooksUI_Operations renew = new Asst_IssuedBooksUI_Operations("renew",userLevel,UserID,
                    issuedBook,issuedDAO,Asst_IssuedBooksUI.this);
            renew.setVisible(true);
        }
    }//GEN-LAST:event_jbtn_RenewActionPerformed

