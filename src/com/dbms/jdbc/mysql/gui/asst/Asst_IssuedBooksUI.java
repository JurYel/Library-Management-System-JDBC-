    private void jtxt_SearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_SearchKeyTyped
       String search = jtxt_Search.getText().trim();
        try{
            if(search.isEmpty()){
                ArrayList<IssuedBooks> list = issuedDAO.getAllIssuedBooks();
                IssuedBooksTableModel model = new IssuedBooksTableModel(list);
                jtbl_IssuedBooks.setModel(model);
            }
            else{
                ArrayList<IssuedBooks> list = issuedDAO.searchIssuedBook(search);
                IssuedBooksTableModel model = new IssuedBooksTableModel(list);
                jtbl_IssuedBooks.setModel(model);
            }
        }
        catch(Exception exc){
            JOptionPane.showMessageDialog(Asst_IssuedBooksUI.this,"Search Failed: "+exc.getMessage(),"Search Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtxt_SearchKeyTyped

