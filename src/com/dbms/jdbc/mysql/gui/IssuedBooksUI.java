    public IssuedBooksUI(int level, String userID,String userName){
        try{
            issuedDAO = new IssuedBooksDAO();
            issuedDAO.overduedBooks();
        }
        catch(Exception exc){
            JOptionPane.showMessageDialog(IssuedBooksUI.this,"DAO Failed: "+exc.getMessage(),"DAO Error",JOptionPane.ERROR_MESSAGE);
        }
        initComponents();
        setLocationRelativeTo(null);
        userLevel = level;
        User_ID = userID;
        UserName = userName;
        populateTable();
        switch(userLevel){
            case 0:
                jlbl_Label.setText("Admin: ");
                jlbl_Online.setText(UserName);
                break;
            case 1:
                jlbl_Label.setText("Asst: ");
                jlbl_Online.setText(UserName);
                break;
            case 2:
                jlbl_Label.setText("Online: ");
                jlbl_Online.setText(UserName);
                break;          
        }
        checkOverduedBooks();
        
    }
    
