    private void jtxt_SearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_SearchKeyTyped
       String key = jtxt_Search.getText().trim();
       
       try{
          if(key.isEmpty()){
            ArrayList<IssuedBooks> list = issuedDAO.getAllIssuedBooks();
            IssuedBooksTableModel model = new IssuedBooksTableModel(list);
            jtbl_IssuedBooks.setModel(model);
            } 
          else{
              ArrayList<IssuedBooks> list = issuedDAO.searchIssuedBook(key);
              IssuedBooksTableModel model = new IssuedBooksTableModel(list);
              jtbl_IssuedBooks.setModel(model);
          }
       }
       catch(Exception exc){
           JOptionPane.showMessageDialog(IssuedBooksUI.this,"Search Failed: "+exc.getMessage(),"Search Error",JOptionPane.ERROR_MESSAGE);
       }
       
    }//GEN-LAST:event_jtxt_SearchKeyTyped

    private void jbtn_RenewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_RenewActionPerformed
        int row = jtbl_IssuedBooks.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(IssuedBooksUI.this,"Select a row","Renew Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            String bookID = jtbl_IssuedBooks.getValueAt(row,0).toString();
            String bookTitle = jtbl_IssuedBooks.getValueAt(row,1).toString();
            String userID = jtbl_IssuedBooks.getValueAt(row,2).toString();
            Date dateBrwd = (Date)jtbl_IssuedBooks.getValueAt(row,3);
            Date datedue = (Date)jtbl_IssuedBooks.getValueAt(row,4);
            
            IssuedBooks issuedBook = new IssuedBooks(bookID,bookTitle,userID,dateBrwd,datedue);
            IssuedBooksUI_Operations renew = new IssuedBooksUI_Operations("renew",userLevel,User_ID,
                    issuedBook,issuedDAO,IssuedBooksUI.this);
            renew.setVisible(true);
        }
    }//GEN-LAST:event_jbtn_RenewActionPerformed

